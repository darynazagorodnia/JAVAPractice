package homeWork3;

public class Numbers {
    // цикли
    public static void main(String[] args) {
        for (int a = 1; a <= 5; a++)
            System.out.println("Номер " + a);

        System.out.println();

        for (int i = 10; i >= 1; i--)
            System.out.println("Номер " + i);
    }
}
