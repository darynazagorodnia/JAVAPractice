package homeWorkForMe;

public class Сycle {

    // Цикдічний оператор while, do-while

    public static void main(String[] args) {
        int a = 5;
        while (a <= 10) {
            System.out.println(a);
            a++;
        }

        int volum = 49;
        do {
            System.out.println("full");
        } while (volum > 50);
    }
}
